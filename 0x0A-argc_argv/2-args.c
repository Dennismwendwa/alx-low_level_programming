#include <stdio.h>
#include <stdlib.h>

/**
  * main - Entry point.
  * @argc:- commandlines args.
  * @argv:- commandline args.
  * Return:- Always 0 (success)
  */

int main(int argc, char *argv[])
{
	int i;


		argc = argc;

		for (i = 0; i < argc; i++)
		{
			printf("%s\n", argv[i]);

		}
	return (0);

}
